// Estrutura repetição com teste lógico no início:
var c = 1
while (c < 5) {
    console.log(`Passo ${c}`)
    c++
}


// Estrutura de repetição com teste lógico no final:
var c = 1
do {
    console.log(`Passo ${c}`)
    c++
} while (c < 5)


// Estrutura de repetição com parametro de controle:
for (var c = 1; c < 10 ; c++) {
    console.log(`Passo ${c}`)
}